package de.samply.file.bundle;

public class PathsBundleException extends Exception {

  public PathsBundleException() {
  }

  public PathsBundleException(String message) {
    super(message);
  }

  public PathsBundleException(String message, Throwable cause) {
    super(message, cause);
  }

  public PathsBundleException(Throwable cause) {
    super(cause);
  }

}
