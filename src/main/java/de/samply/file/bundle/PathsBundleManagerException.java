package de.samply.file.bundle;

public class PathsBundleManagerException extends Exception {

  public PathsBundleManagerException(String message) {
    super(message);
  }

  public PathsBundleManagerException(String message, Throwable cause) {
    super(message, cause);
  }

  public PathsBundleManagerException(Throwable cause) {
    super(cause);
  }

  public PathsBundleManagerException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

}
